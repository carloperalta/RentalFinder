       <h4> 1. Names of all tenants.</h4>
        Every adult who lives in the rental unit, including both members of a married or unmarried couple, should be named as tenants and sign the lease or rental agreement. This makes each tenant legally responsible for all terms, including the full amount of the rent and the proper use of the property. This means that you can legally seek the entire rent from any one of the tenants should the others skip out or be unable to pay; and if one tenant violates an important term of the tenancy, you can terminate the tenancy for all tenants on that lease or rental agreement. <br>

<h4>2. Limits on occupancy.</h4> 
Your agreement should clearly specify that the rental unit is the residence of only the tenants who have signed the lease and their minor children. This guarantees your right to determine who lives in your property -- ideally, people whom you have screened and approved -- and to limit the number of occupants. The value of this clause is that it gives you grounds to evict a tenant who moves in a friend or relative, or sublets the unit, without your permission. <br>


<h4>3. Term of the tenancy.</h4>
 Every rental document should state whether it is a rental agreement or a fixed-term lease. Rental agreements usually run from month-to-month and self-renew unless terminated by the landlord or tenant. Leases, on the other hand, typically last a year. Your choice will depend on how long you want the tenant to stay and how much flexibility you want in your arrangement. <br>

<h4> 4. Rent. </h4> 
Your lease or rental agreement should specify the amount of rent, when it is due (typically, the first of the month), and how it's to be paid, such as by mail to your office. To avoid confusion and head off disputes with tenants, spell out details such as: <br>

acceptable payment methods (such as personal check only)
whether late fees will be due if rent is not paid on time, the amount of the fee, and whether there's any grace period, and
any charges if a rent check bounces. <br>
<h4>5. Deposits and fees.</h4> 
The use and return of security deposits is a frequent source of friction between landlords and tenants. To avoid confusion and legal hassles, your lease or rental agreement should be clear on the limit, use and return of deposits, including:<br>

the dollar amount of the security deposit (be sure you comply with any state laws setting maximum amounts)
how you may use the deposit (for example, for damage repair) and how the tenant may not use it (such as applying it to last month's rent)
when and how you will return the deposit and account for deductions after the tenant moves out, and
any legal non-returnable fees, such as for cleaning or pets.
It's also a good idea (and legally required in a few states and cities) to include details on where the security deposit is being held and whether interest on the security deposit will be paid to the tenant. <br>

<h4>6. Repairs and maintenance. </h4>
 Your best defense against rent-withholding hassles and other problems (especially over security deposits) is to clearly set out your and the tenant's responsibilities for repair and maintenance in your lease or rental agreement, including: <br>

the tenant's responsibility to keep the rental premises clean and sanitary and to pay for any damage caused by his or her abuse or neglect
a requirement that the tenant alert you to defective or dangerous conditions in the rental property, with specific details on your procedures for handling complaint and repair requests, and
restrictions on tenant repairs and alterations, such as adding a built-in dishwasher, installing a burglar alarm system, or painting walls without your permission.
7. Entry to rental property. To avoid tenant claims of illegal entry or violation of privacy rights, your lease or rental agreement should clarify your legal right of access to the property -- for example, to make repairs -- and state how much advance notice you will provide the tenant before entering. <br>

<h4>8. Restrictions on tenant illegal activity.</h4>
 To avoid trouble among your tenants, prevent property damage, and limit your exposure to lawsuits from residents and neighbors, you should include an explicit lease or rental agreement clause prohibiting disruptive behavior, such as excessive noise, and illegal activity, such as drug dealing.<br>

